/**
 * a representation of a wood animal puzzle
 * 
 * @author Daniel Chavez
 */
public class WoodAnimalPuzzle extends Puzzle {

  public WoodAnimalPuzzle(){
    this.name = "Animal Puzzle by Melissa and Doug";
    this.material = "wood";
    pieces.add("Horse");
    pieces.add("Sheep");
    pieces.add("Dog");
    pieces.add("Cat");
    pieces.add("Cow");
    pieces.add("Chicken");
  }
}