/**
 * a representation of a plastic animal puzzle
 * @author Daniel Chavez
 */
public class PlasticAnimalPuzzle extends Puzzle {

  public PlasticAnimalPuzzle() {
    this.name = "Animal Puzzle by Fisher Price";
    this.material = "plastic";
    pieces.add("Fox");
    pieces.add("Elephant");
    pieces.add("Giraffe");
    pieces.add("Owl");
    pieces.add("Monkey");
  }
}